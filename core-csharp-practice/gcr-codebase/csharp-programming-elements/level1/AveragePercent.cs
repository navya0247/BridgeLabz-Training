using System;
class AveragePercent{
	public static void Main(String[]args){
		int maths=94;                                       //sam's maths mark
		int physics=95;                                     //sam's physics mark
		int chemistry=96;                                   //sam's chemistry mark
		int total=(maths+physics+chemistry)/3;           //calculate the average percent
		Console.WriteLine("Sam's average mark in PCM is "+total);
	}
}
		